<script>

// Toggle Job Details
function toggleDetails(jobId) {
    const jobItem = document.getElementById(jobId);
    const jobDetails = jobItem.querySelector('.job-details');
    const toggleIcon = jobItem.querySelector('.toggle-icon');

    if (jobDetails.classList.contains('hidden')) {
        jobDetails.classList.remove('hidden');
        toggleIcon.textContent = '-';
    } else {
        jobDetails.classList.add('hidden');
        toggleIcon.textContent = '+';
    }
}


// Open the modal
function openModal() {
    var modal = document.getElementById("jobModal");
    modal.style.display = "block";
}

// Close the modal
function closeModal() {
    var modal = document.getElementById("jobModal");
    modal.style.display = "none";
}

// Close the modal if clicked outside the modal content
window.onclick = function(event) {
    var modal = document.getElementById("jobModal");
    if (event.target == modal) {
        modal.style.display = "none";
    }
}



</script>
